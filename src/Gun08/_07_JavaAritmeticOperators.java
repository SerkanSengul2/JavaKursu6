public class _07_JavaAritmeticOperators {
    public static void main(String[] args) {

        int a = 10;//a ya 10 degerı atandı
        a = 5;// a ya 5 degerı atandı

        a = a + 6;//a ya 6 degerını ekle 11 oldu
        a += 6;//a ya 6 degerı eklendı 17 oldu

        a++; //a yı 1 arttır
        ++a; //a yı 1 arttır
        a = +1; //a yı 1 arttır

        /**********************/
        a = a - 1;//a yı 1 azalt
        a--;//a yı 1 azalt
        --a;// a yı 1 azalt
        a -= 1;//a yı 1 azalt

        /****************************/

        int b = 10;
        b = b / 5; //b yi 5e böl
        b /= 5; //b yi 5e böl

        /**************************/

        int c=10;
        c=c*5; //c yi 5 ile çarp
        c*=5;  //c yi 5 ile çarp





    }


}
